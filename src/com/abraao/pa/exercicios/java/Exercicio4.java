public class Exercicio4 {

	public static void main(String[] args) {

		// Faça um programa que exiba na tela a média aritmética entre os números 8,
		// 9 e 7.
		
		int c = 8;
		int d = 9;
		int e = 7;
		int media;

		media = (c + d + e) / 2;

		System.out.println("========================");
		System.out.println("Média" + media);

	}
}
