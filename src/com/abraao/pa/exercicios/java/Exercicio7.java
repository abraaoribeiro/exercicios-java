public class Exercicio7 {

	public static void main(String[] args) {

		// Faça um programa que leia um número inteiro e exiba na tela seu sucessor e
		// seu antecessor.

		int h = 4;
		int sucessor;
		int antercessor;

		antercessor = h - 1;
		sucessor = h + 1;
		System.out.println("========================");
		System.out.println("antercessor: " + antercessor + "\nsucessor: " + sucessor);
	}
}
